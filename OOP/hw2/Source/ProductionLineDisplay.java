//***************************************************************************************************************************************************

import javax.swing.JPanel  ;
import java.awt.Color      ;
import java.awt.Graphics   ;
import java.awt.Dimension  ;
import java.awt.Graphics2D ;

//***************************************************************************************************************************************************

public class ProductionLineDisplay extends JPanel
{
  //=================================================================================================================================================

  private static final long serialVersionUID = 4697429510609174848L ;

  //=================================================================================================================================================

  public ProductionLineDisplay ()
  {
    setBackground( Color.WHITE ) ;
  }

  //=================================================================================================================================================

  @Override public Dimension getPreferredSize ()
  {
    return new Dimension( Runner.factory.productionLine.maxCapacity * 200 , 200 ) ;
  }

  //=================================================================================================================================================

  @Override public void paintComponent ( Graphics g )
  {
    super.paintComponent( g ) ;

    Graphics2D g2d = (Graphics2D) g ;

    Runner.factory.productionLine.draw( g2d ) ;
  }

  //=================================================================================================================================================
}

//***************************************************************************************************************************************************
